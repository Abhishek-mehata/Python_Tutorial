# Upper Case
a = "Hello, World!"
print(a.upper())

# ------------------------------------------------------------------------------------------------------------------------------------------

# Lower Case
a = "Hello, World!"
print(a.lower())

# ------------------------------------------------------------------------------------------------------------------------------------------

# Remove Whitespace before and/or after the actual text
a = " Hello, World! "
print(a.strip()) # returns "Hello, World!"


# ------------------------------------------------------------------------------------------------------------------------------------------

# Replace String
a = "Hello, World!"
print(a.replace("H", "P"))


# ------------------------------------------------------------------------------------------------------------------------------------------

# Split String

a = "Hello, World!"
print(a.split(",")) # returns ['Hello', ' World!']


# ------------------------------------------------------------------------------------------------------------------------------------------

