# Assign String to a Variable
a = "Hello"
print(a)

# -------------------------------------------------------------------------------------------------------------------------------------

# Multiline Strings

a = """
    Hi i am abhi,
    I read in class 11,
    I am a great coder
    """
print(a)

# ------------------------------------------------------------------------------------------------------------------------------------

# Strings are Arrays
# We can alse treat strings as an array.

a = "Hello, World!"
print(a[0])  # Output: 'H'
print(a[1])  # Output: 'e'
print(a[2])  # Output: 'l'
print(a[3])  # Output: 'l'
print(a[4])  # Output: 'o'

# -------------------------------------------------------------------------------------------------------------------------------------

# String Length
# Using len() function you can get length of a string

a = "Hello, World!"
print(len(a))  # Output:13

# -------------------------------------------------------------------------------------------------------------------------------------

